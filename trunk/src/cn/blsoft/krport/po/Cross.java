public class Cross {

	private String crossText;
	
	private String rowField;
	
	private String colField;
	
	private String valueField;
	
	private Boolean rowCount;
	
	private Boolean colCount;

	/**
	 * @return the crossText
	 */
	public String getCrossText() {
		return crossText;
	}

	/**
	 * @param crossText the crossText to set
	 */
	public void setCrossText(String crossText) {
		this.crossText = crossText;
	}

	/**
	 * @return the rowField
	 */
	public String getRowField() {
		return rowField;
	}

	/**
	 * @param rowField the rowField to set
	 */
	public void setRowField(String rowField) {
		this.rowField = rowField;
	}

	/**
	 * @return the colField
	 */
	public String getColField() {
		return colField;
	}

	/**
	 * @param colField the colField to set
	 */
	public void setColField(String colField) {
		this.colField = colField;
	}

	/**
	 * @return the valueField
	 */
	public String getValueField() {
		return valueField;
	}

	/**
	 * @param valueField the valueField to set
	 */
	public void setValueField(String valueField) {
		this.valueField = valueField;
	}

	/**
	 * @return the rowCount
	 */
	public Boolean getRowCount() {
		return rowCount;
	}

	/**
	 * @param rowCount the rowCount to set
	 */
	public void setRowCount(Boolean rowCount) {
		this.rowCount = rowCount;
	}

	/**
	 * @return the colCount
	 */
	public Boolean getColCount() {
		return colCount;
	}

	/**
	 * @param colCount the colCount to set
	 */
	public void setColCount(Boolean colCount) {
		this.colCount = colCount;
	}

	
}
